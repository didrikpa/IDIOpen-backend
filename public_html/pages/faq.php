<h1>
FAQ
</h1>
<p>
<strong>Are we allowed to be less than three members on the team?</strong>
</p>
<p>
Yes, A team can consist of one, two or three people.
</p>
<br>

<p>
<strong>What programming languages can we use on the contest?</strong>
</p>
<p>The supported languages are C, C++ and Java.</p>
<br>

<p>
<strong>My program doesn't compile on the server, although it does locally! What might be the problem?</strong>
</p>
<p>If you're programming in Java, make sure you don't have any package declaration on you class. Otherwise, the compile options used for all three supported languages are shown below:</p>
<table>
<tr><th>Language</th><th>Compilation</th></tr>
<tr><td><strong>C</strong></td><td><i>gcc -w --std=c99 -O2 -o {BASENAME} {FILENAME} -lm</i></td></tr>
<tr><td><strong>C++</strong></td><td><i>g++ -w -O2 -o {BASENAME} {FILENAME}</i></td></tr>
<tr><td><strong>Java</strong></td><td><i>javac -nowarn {FILENAME}</i></td></tr>
</table>
