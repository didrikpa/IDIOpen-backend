<h1>
IDI Open - History
</h1>
</br>
<p>

The first IDI Open Programming Contest was held in 2007. It was Rune Johan Hovland and Eirik Reksten who came up with the idea of holding a contest in the spring. They kind of missed competing, as the only other contest on NTNU, NCPC, is held in the fall. Dr. Anne C. Elster was co-founder and
served as faculty advisor 2007-2010. Magnus Lie Hetland is currently serving as faculty advisor.

</p>
</br>

