<h1>
Contest Description
</h1>
</br>
<p>

IDI Open is a programming contest where teams of up to three people meet and 
solves programming problems of various types. The contest lasts five hours, 
and the objective is to solve as many problems as you can.
The contest is open for all types of programmers, from students from all 
grades to professors and professionals. Prizes aren't valid for all types of 
teams, though, so you might want to check this out before gathering up a 
team (if that's a motive).

</p>

<p>

There are usually 8-12 problems in a contest.The problems are generally of very varying difficulty, from very easy, 
to very hard. Most teams (that know how to write simple programs) should 
be more than capable of solving at least one problem within the time 
limits, while all teams also should find at least some of the problems challenging.

</p>

